// authoringToolHint is the one-paragraph nudge appended to the create_page /
// update_page tool descriptions (shown at call time — the strongest signal). It
// names the rich blocks so the agent knows the palette exists, and points at the
// guide for exact syntax. Block names derive from the manifest so the hint can't
// drift from what the reader supports.
func authoringToolHint() string {
	var names []string
	for _, b := range authoringBlocks {
		if b.Agent {
			names = append(names, strings.ToLower(b.Label))
		}
	}
	return "tela renders a rich block palette beyond plain markdown — " +
		strings.Join(names, ", ") +
		". Prefer these over walls of text; read the tela://authoring-guide resource (or this server's instructions) for exact syntax."
}
